/**
  * @brief This function handles UART4 global interrupt.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */
	uint32_t tmp_flag = 0;
	uint32_t temp;
	tmp_flag = __HAL_UART_GET_FLAG(&huart4, UART_FLAG_IDLE);
	if ((tmp_flag != RESET))
	{ 
		__HAL_UART_CLEAR_IDLEFLAG(&huart4); 
		temp = huart4.Instance->SR; 
		temp = huart4.Instance->DR; 
		HAL_UART_DMAStop(&huart4); 
		temp  = __HAL_DMA_GET_COUNTER(&hdma_uart4_rx); 
		u8 UART4_Rec_Len =  UART4RXLen - temp; 
		if (UART4_Rec_Len >= 14)
		{
			for (int i = 0; i < 40; ++i)
			{
				if (UART4RXCache[i] == 'T')
				{
					for (int j = i; j < 40; ++j)
					{
						if (UART4RXCache[j] == '\n')
						{
							UART4RXPosition = &UART4RXCache[i];
							UART4EndFlag = 1; 
							__HAL_UART_DISABLE_IT(&huart4, UART_IT_IDLE);
							xTaskResumeFromISR(WaterDeepTaskHandle);
							break;
						}
					}
					break;
				}
			}
			if (UART4EndFlag == 0)
			{
				HAL_UART_Receive_DMA(&huart4, UART4RXCache, UART4RXLen);
			}
		}
		else
		{
			HAL_UART_Receive_DMA(&huart4, UART4RXCache, UART4RXLen);
		}
	}
  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */

  /* USER CODE END UART4_IRQn 1 */
}

